# Random Forest Hyper-Heuristic (RF-HH)
# ----------------------------------------------------------------------
def train_rf(df: pd.DataFrame) -> RandomForestClassifier:
    """Entrena un modelo Random Forest usando el dataset generado.

    Args:
        df (pd.DataFrame): El DataFrame de entrenamiento con características y etiquetas.

    Returns:
        RandomForestClassifier: El modelo de Random Forest entrenado.
    """
    X = df[FEATURE_NAMES].values # Características (inputs) del modelo.
    y = df["BEST_H"].map(H_TO_IDX).values # Etiquetas (outputs) del modelo, mapeadas a índices numéricos.
    model = RandomForestClassifier(
        n_estimators=N_ESTIMATORS, # Número de árboles en el bosque.
        min_samples_leaf=MIN_SAMPLES_LEAF, # Mínimo de muestras por hoja.
        random_state=SEED, # Semilla para reproducibilidad.
        n_jobs=1,  # 1 núcleo: evita el overhead de joblib en predicciones de una sola muestra.
    )
    model.fit(X, y) # Entrena el modelo.
    return model


def solve_rf_hh(state: KnapsackState, model: RandomForestClassifier,
                threshold: float = CONFIDENCE_THRESHOLD) -> int:
    """Solucionador RF-HH con fallback a MAXPW basado en el umbral de confianza.

    Cuando la probabilidad de la clase principal predicha por el clasificador está por debajo del `threshold`,
    se usa la heurística segura por defecto, MAXPW. Esto protege contra predicciones de baja confianza
    en estados que no están bien cubiertos por los datos de entrenamiento.

    Args:
        state (KnapsackState): El estado actual del problema de la mochila.
        model (RandomForestClassifier): El modelo de Random Forest entrenado.
        threshold (float): Umbral de confianza para el fallback.

    Returns:
        int: El beneficio final obtenido.
    """
    classes = list(model.classes_) # Clases (heurísticas) que el modelo puede predecir.
    while state.remaining.any(): # Mientras queden ítems por considerar.
        feats = np.array([step_features(state)]) # Calcula las características para el estado actual.
        proba = model.predict_proba(feats)[0] # Obtiene las probabilidades de predicción para cada heurística.
        k = int(np.argmax(proba)) # Índice de la heurística con mayor probabilidad.
        h_idx = classes[k] # Índice de la heurística predicha.
        # Si la confianza es baja Y la heurística NO es MAXPW, se fuerza a MAXPW (fallback).
        if h_idx != MAXPW_IDX and proba[k] < threshold:
            h_idx = MAXPW_IDX
        item = state.next_item(HEURISTICS[h_idx]) # Obtiene el ítem seleccionado por la heurística (predicha o fallback).
        if item == -1: # Si no se puede empacar más ítems.
            break
        state.pack(item) # Empaca el ítem.
    return state.profit


def solve_rf_hh_batch(states: list[KnapsackState],
                      model: RandomForestClassifier,
                      threshold: float = CONFIDENCE_THRESHOLD) -> list[int]:
    """Resuelve muchas instancias en paralelo con una sola llamada a `predict_proba` por paso.

    Produce exactamente las mismas soluciones que `solve_rf_hh`; el procesamiento por lotes solo
    amortiza la sobrecarga por llamada del predictor de scikit-learn (una llamada de una sola fila cuesta casi lo mismo
    que una llamada de varios cientos de filas), por lo que el rendimiento mejora en órdenes de magnitud.

    Args:
        states (list[KnapsackState]): Lista de estados del problema de la mochila.
        model (RandomForestClassifier): El modelo de Random Forest entrenado.
        threshold (float): Umbral de confianza para el fallback.

    Returns:
        list[int]: Una lista con los beneficios finales obtenidos para cada instancia.
    """
    classes = list(model.classes_)
    # `active` guarda los índices de las instancias que aún tienen ítems restantes.
    active = [i for i, s in enumerate(states) if s.remaining.any()]
    while active:
        # Calcula las características para todos los estados activos en un solo lote.
        feats = np.array([step_features(states[i]) for i in active])
        probas = model.predict_proba(feats) # Predice las probabilidades para todo el lote.
        still_active = []
        for row, i in enumerate(active):
            s = states[i]
            proba = probas[row]
            k = int(np.argmax(proba))
            h_idx = classes[k]
            # Aplica la lógica de fallback como en `solve_rf_hh`.
            if h_idx != MAXPW_IDX and proba[k] < threshold:
                h_idx = MAXPW_IDX
            item = s.next_item(HEURISTICS[h_idx])
            if item == -1:
                continue
            s.pack(item)
            if s.remaining.any(): # Si esta instancia aún tiene ítems restantes, la añade a la lista de `still_active`.
                still_active.append(i)
        active = still_active # Actualiza la lista de instancias activas.
    return [s.profit for s in states]


