# Figuras (todas guardadas a 300 dpi en `outdir`)
# ----------------------------------------------------------------------
def make_figures(df, model, oracle_first, rf_first, outdir):
    """Genera varias figuras para visualizar los resultados y las guarda en el directorio especificado.

    Args:
        df (pd.DataFrame): DataFrame con los resultados de profit.
        model (RandomForestClassifier): El modelo RF-HH entrenado.
        oracle_first (list): Decisiones del primer paso del oráculo (rollout).
        rf_first (list): Decisiones del primer paso del RF-HH.
        outdir (str): Directorio donde se guardarán las figuras.
    """
    os.makedirs(outdir, exist_ok=True) # Crea el directorio de salida si no existe.
    methods = HEURISTICS + ["Rollout", "RF-HH"]
    sns.set_style("whitegrid") # Establece el estilo de las gráficas de seaborn.

    # Fig 2: Diagrama de cajas (boxplot) de los profits de cada método.
    plt.figure(figsize=(10, 6))
    sns.boxplot(data=df[methods], palette="viridis")
    plt.xlabel("Método")
    plt.ylabel("Beneficio")
    plt.savefig(os.path.join(outdir, "boxplot.png"), **FIG_KW)
    plt.close() # Cierra la figura para liberar memoria.

    # Fig 3: Gráfico de dispersión (scatter) de MAXPW vs Rollout.
    plt.figure(figsize=(8, 8))
    # Define los límites para los ejes, asegurando que cubran todos los datos.
    lims = [min(df["MAXPW"].min(), df["Rollout"].min()),
            max(df["MAXPW"].max(), df["Rollout"].max())]
    plt.scatter(df["MAXPW"], df["Rollout"], alpha=0.5, color="#2a788e")
    plt.plot(lims, lims, "r--", alpha=0.75, label="x = y") # Línea de referencia donde ambos métodos son iguales.
    plt.xlabel("Beneficio MAXPW")
    plt.ylabel("Beneficio Rollout")
    plt.legend()
    plt.savefig(os.path.join(outdir, "scatter.png"), **FIG_KW)
    plt.close()

    # Fig 4: Distribuciones de profit de MAXPW y RF-HH.
    plt.figure(figsize=(10, 6))
    sns.kdeplot(df["MAXPW"], fill=True, label="MAXPW", color="blue")
    sns.kdeplot(df["RF-HH"], fill=True, label="RF-HH", color="green")
    plt.xlabel("Beneficio")
    plt.ylabel("Densidad")
    plt.legend()
    plt.savefig(os.path.join(outdir, "distribution.png"), **FIG_KW)
    plt.close()

    # Fig 5: Mapa de calor (heatmap) de las elecciones de heurística en el primer paso por tipo de instancia.
    hm = pd.DataFrame({
        "Type": df["Type"],
        "Heuristic": [HEURISTICS[i] for i in oracle_first], # Convierte índices a nombres de heurísticas.
    })
    # Calcula las frecuencias de cada heurística por tipo de instancia.
    counts = hm.groupby(["Type", "Heuristic"]).size().unstack(fill_value=0)
    plt.figure(figsize=(10, 6))
    sns.heatmap(counts, annot=True, fmt="d", cmap="YlGnBu") # `annot=True` muestra los valores en las celdas.
    plt.xlabel("Heurística seleccionada por el rollout")
    plt.ylabel("Tipo de instancia")
    plt.savefig(os.path.join(outdir, "heatmap.png"), **FIG_KW)
    plt.close()

    # Fig 6: trayectoria de profit acumulado en una instancia representativa (se omite en el CLI).
    # (resolver la primera instancia paso a paso)
    # Se deja al notebook si es necesario; omitido aquí para la brevedad del CLI.

    # Fig 7: Importancia de las características (feature importances) del modelo RF-HH.
    imp = model.feature_importances_ # Obtiene la importancia de cada característica.
    order = np.argsort(imp) # Ordena las características por importancia.
    plt.figure(figsize=(10, 6))
    # Crea un gráfico de barras horizontales mostrando la importancia.
    plt.barh(range(len(order)), imp[order], color="#2a788e")
    plt.yticks(range(len(order)), [FEATURE_NAMES[i] for i in order]) # Nombres de las características en el eje Y.
    plt.xlabel("Importancia relativa")
    plt.tight_layout() # Ajusta el diseño para evitar solapamientos.
    plt.savefig(os.path.join(outdir, "feature_importance.png"), **FIG_KW)
    plt.close()

    # Fig 8: Matriz de confusión (decisiones del primer paso en el conjunto de prueba).
    # Compara las decisiones del primer paso del Rollout (oráculo) con las del RF-HH.
    labels = np.unique(np.concatenate([oracle_first, rf_first])) # Obtiene todas las etiquetas únicas.
    cm = confusion_matrix(
        oracle_first, rf_first, labels=labels) # Calcula la matriz de confusión.
    disp = ConfusionMatrixDisplay(
        cm, display_labels=[HEURISTICS[i] for i in labels]) # Prepara la visualización.
    fig, ax = plt.subplots(figsize=(8, 8))
    disp.plot(cmap="Blues", ax=ax, colorbar=False)
    plt.savefig(os.path.join(outdir, "confusion_matrix.png"), **FIG_KW)
    plt.close()

    # Fig 9: Comparación del rendimiento por dificultad (EASY/HARD).
    diff = (df.groupby("Category")[["MAXPW", "Rollout", "RF-HH"]]
              .mean().reset_index()
              .melt(id_vars="Category", var_name="Method",
                    value_name="Mean Profit")) # Agrupa, calcula el promedio y transforma el formato.
    plt.figure(figsize=(10, 6))
    sns.barplot(x="Category", y="Mean Profit", hue="Method",
                data=diff, palette="viridis") # Crea un gráfico de barras.
    plt.savefig(os.path.join(outdir, "difficulty_comparison.png"), **FIG_KW)
    plt.close()

    print(f"Figuras escritas en {outdir}/")


