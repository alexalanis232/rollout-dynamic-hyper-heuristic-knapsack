# ----------------------------------------------------------------------
# Estado del problema (basado en arrays, no necesita deepcopy)
# ----------------------------------------------------------------------
class KnapsackState:
    """Estado de la mochila 0/1 basado en arrays.

    Los ítems se almacenan una vez como arrays de numpy; el proceso de resolución solo
    modifica `remaining` (máscara booleana) y `capacity`. Así, simular un
    rollout es una operación barata de guardar/restaurar esos dos valores en lugar de una
    copia profunda de todo el objeto.
    """

    __slots__ = ("weights", "profits", "ratios", "ratio_order",
                 "remaining", "capacity", "initial_capacity",
                 "n_items", "profit")

    def __init__(self, weights, profits, capacity):
        # Pesos de los ítems.
        self.weights = np.asarray(weights, dtype=np.int64)
        # Beneficios de los ítems.
        self.profits = np.asarray(profits, dtype=np.int64)
        # Razón beneficio/peso de cada ítem.
        self.ratios = self.profits / self.weights
        # Orden de los ítems basado en la razón beneficio/peso (de mayor a menor).
        # Se usa un ordenamiento estable para romper empates por el índice original.
        self.ratio_order = np.argsort(-self.ratios, kind="stable")
        # Número total de ítems.
        self.n_items = len(self.weights)
        # Máscara booleana que indica qué ítems aún no han sido empacados.
        self.remaining = np.ones(self.n_items, dtype=bool)
        # Capacidad actual de la mochila.
        self.capacity = int(capacity)
        # Capacidad inicial de la mochila (útil para el cálculo de características).
        self.initial_capacity = int(capacity)
        # Beneficio total acumulado en la mochila.
        self.profit = 0

    # -- E/S -----------------------------------------------------------
    @classmethod
    def from_file(cls, path: str) -> "KnapsackState":
        """Carga un estado del problema de la mochila desde un archivo.

        El archivo debe tener la primera línea con 'num_items, capacity'
        y las siguientes líneas con 'weight, profit' por cada ítem.
        """
        with open(path) as f:
            lines = f.readlines()
        # Lee el número de ítems y la capacidad de la primera línea.
        n_items, capacity = (int(x.strip()) for x in lines[0].split(","))
        weights, profits = [], []
        # Lee los pesos y beneficios de los ítems de las líneas siguientes.
        for i in range(n_items):
            w, p = lines[i + 1].split(",")
            weights.append(int(w.strip()))
            profits.append(int(float(p.strip())))
        return cls(weights, profits, capacity)

    # -- Selección de ítems por heurística -------------------------------
    def next_item(self, heuristic: str) -> int:
        """Devuelve el índice del ítem que la heurística empacaría a continuación, o -1 si no hay ninguno.

        Args:
            heuristic (str): El nombre de la heurística a usar ('DEF', 'MAXP', 'MAXPW', 'MINW').

        Returns:
            int: El índice del siguiente ítem a empacar, o -1.
        """
        # Encuentra los ítems que aún no han sido empacados y caben en la mochila.
        fits = self.remaining & (self.weights <= self.capacity)
        # Si no hay ítems que quepan, devuelve -1.
        if not fits.any():
            return -1
        # Obtiene los índices de los ítems que caben.
        idx = np.flatnonzero(fits)

        # Aplica la lógica de cada heurística:
        if heuristic == "DEF": # Default (el primer ítem que cabe).
            return int(idx[0])
        if heuristic == "MAXP": # Máximo Beneficio (el ítem con mayor beneficio).
            return int(idx[np.argmax(self.profits[idx])])
        if heuristic == "MAXPW": # Máxima Razón Beneficio/Peso (el ítem con mayor razón p/w).
            return int(idx[np.argmax(self.ratios[idx])])
        if heuristic == "MINW": # Mínimo Peso (el ítem con menor peso).
            return int(idx[np.argmin(self.weights[idx])])
        raise ValueError(f"Heurística desconocida: {heuristic}")

    def pack(self, item: int) -> None:
        """Empaca un ítem en la mochila, actualizando el estado.

        Args:
            item (int): El índice del ítem a empacar.
        """
        # Marca el ítem como no disponible.
        self.remaining[item] = False
        # Reduce la capacidad de la mochila.
        self.capacity -= int(self.weights[item])
        # Aumenta el beneficio total.
        self.profit += int(self.profits[item])

    # -- Completado rápido con MAXPW (usado por el rollout) --------------
    def maxpw_completion_profit(self) -> int:
        """Calcula el beneficio total después de completar el estado actual de forma voraz con MAXPW.

        Iterar la lista de ítems preordenada por razón beneficio/peso y empacar lo que quepa
        es equivalente a seleccionar repetidamente el ítem con mejor razón que quepa;
        el estado en sí no se modifica.

        Returns:
            int: El beneficio total (actual + el obtenido por la completación MAXPW).
        """
        cap = self.capacity
        extra = 0 # Beneficio adicional de la completación.
        w, p, rem = self.weights, self.profits, self.remaining
        for j in self.ratio_order:
            if rem[j] and w[j] <= cap: # Si el ítem restante cabe.
                cap -= w[j]
                extra += p[j]
        return self.profit + extra

    # -- Solucionadores --------------------------------------------------
    def solve_static(self, heuristic: str) -> int:
        """Resuelve el problema de la mochila usando una heurística estática (repetitiva).

        Args:
            heuristic (str): La heurística a usar.

        Returns:
            int: El beneficio final obtenido.
        """
        item = self.next_item(heuristic)
        while item != -1:
            self.pack(item)
            item = self.next_item(heuristic)
        return self.profit


# ----------------------------------------------------------------------
# Política de Rollout
# ----------------------------------------------------------------------
def rollout_choice(state: KnapsackState, base: str = "MAXPW",
                   prefer_base_on_ties: bool = False) -> str:
    """Determina la mejor heurística para el paso actual según la política de rollout.

    Cada heurística empaca un ítem; los ítems restantes se completan
    con la política base (MAXPW). Dado que MAXPW está entre los candidatos,
    el beneficio del rollout nunca es inferior al beneficio del MAXPW puro
    (propiedad de mejora secuencial, Bertsekas et al. 1997).

    Args:
        state (KnapsackState): El estado actual del problema de la mochila.
        base (str): La heurística base para la completación del rollout.
        prefer_base_on_ties (bool): Si True, cuando la heurística base empata
            con el mejor beneficio simulado, se devuelve la base. Usar True para
            ETIQUETAR datos de entrenamiento (evita etiquetas espurias por
            empates) y False al usar el rollout como método de solución.

    Returns:
        str: El nombre de la heurística que produce el mejor resultado en este paso.
    """
    best_h, best_profit = base, -1 # Inicializa con la heurística base.
    base_profit = -1
    for h in HEURISTICS:
        # Simula la selección de un ítem con la heurística actual.
        item = state.next_item(h)
        if item == -1: # Si la heurística no puede empacar nada en este paso.
            continue
        # Simular: empacar el ítem, evaluar la completación, restaurar el estado.
        state.pack(item) # Empaca temporalmente el ítem.
        total = state.maxpw_completion_profit() # Calcula el beneficio total con la completación MAXPW.
        # Deshace el empacado para restaurar el estado original.
        state.remaining[item] = True
        state.capacity += int(state.weights[item])
        state.profit -= int(state.profits[item])
        if h == base:
            base_profit = total
        # Si este resultado es mejor que el actual mejor, actualiza.
        if total > best_profit:
            best_profit, best_h = total, h
    # Desempate: si la base logra el mismo beneficio que la mejor, prefiere la base.
    if prefer_base_on_ties and base_profit == best_profit:
        return base
    return best_h


def solve_rollout(state: KnapsackState, base: str = "MAXPW") -> int:
    """Resuelve el problema de la mochila usando la política de rollout.

    En cada paso, usa `rollout_choice` para decidir la mejor heurística.

    Args:
        state (KnapsackState): El estado inicial del problema de la mochila.
        base (str): La heurística base para la completación en `rollout_choice`.

    Returns:
        int: El beneficio final obtenido.
    """
    while state.remaining.any(): # Mientras queden ítems por considerar.
        h = rollout_choice(state, base) # Elige la mejor heurística para este paso.
        item = state.next_item(h) # Obtiene el ítem seleccionado por esa heurística.
        if item == -1: # Si no se puede empacar más ítems.
            break
        state.pack(item) # Empaca el ítem seleccionado.
    return state.profit


# ----------------------------------------------------------------------
# Características dinámicas del estado (Sección 3.2 del artículo)
# ----------------------------------------------------------------------
def step_features(state: KnapsackState) -> list[float]:
    """Calcula un conjunto de características dinámicas del estado actual del problema de la mochila.

    Estas características se usan como entrada para el modelo de aprendizaje automático.

    Args:
        state (KnapsackState): El estado actual del problema de la mochila.

    Returns:
        list[float]: Una lista de valores de las características.
    """
    rem = state.remaining # Máscara de ítems restantes.
    if not rem.any(): # Si no quedan ítems, todas las características son 0.
        return [0.0] * len(FEATURE_NAMES)

    # Filtra pesos, beneficios y razones para los ítems restantes.
    w = state.weights[rem].astype(float)
    p = state.profits[rem].astype(float)
    r = p / w
    # Razón máxima, normalizada para evitar divisiones por cero.
    rmax = r.max() if r.max() > 0 else 1.0
    n = len(w) # Número de ítems restantes.
    cap = state.capacity # Capacidad restante.

    fits = w <= cap # Ítems restantes que caben en la mochila.
    if fits.any():
        pr = r[fits] # Razones de los ítems que caben.
        # Característica: Razón máxima p/w de ítems empacables, normalizada.
        max_ratio_pack = pr.max() / rmax
        # Característica: Desviación estándar de razones p/w de ítems empacables, normalizada.
        std_ratio_pack = pr.std() / rmax
        # Característica: Fracción de ítems empacables.
        frac_packable = fits.sum() / n
        # Peso medio de los ítems empacables.
        mean_w_pack = w[fits].mean()
    else:
        max_ratio_pack = std_ratio_pack = frac_packable = 0.0
        mean_w_pack = 1.0

    # Característica: Tightness (estrechez), relación entre la capacidad y el peso total de los ítems restantes.
    tightness = cap / w.sum() if w.sum() > 0 else 0.0
    # Característica: Fill (llenado), fracción de la capacidad inicial ya ocupada.
    fill = 1.0 - cap / state.initial_capacity
    # Característica: Slack per item (holgura por ítem), capacidad restante dividida por el peso medio
    # de los ítems empacables, normalizada por el número de ítems.
    slack_per_item = (cap / mean_w_pack) / n if mean_w_pack > 0 else 0.0
    # Característica: Fracción de ítems restantes respecto al total inicial.
    return [r.mean() / rmax, max_ratio_pack, std_ratio_pack,
            frac_packable, tightness, fill, slack_per_item,
            n / state.n_items]


