def instance_category(filename: str) -> str:
    """Extrae la categoría de dificultad y la heurística generadora del nombre de archivo de la instancia.

    Args:
        filename (str): El nombre del archivo de la instancia.

    Returns:
        str: Una cadena que describe la categoría (ej. 'DEF_EASY', 'MAXPW_HARD').
    """
    for h in HEURISTICS:
        for d in ("EASY", "HARD"):
            if f"{h}_{d}" in filename:
                return f"{h}_{d}"
    return "OTHER"


def evaluate(test_folder: str, model: RandomForestClassifier,
             outdir: str) -> tuple[pd.DataFrame, float, float]:
    """Evalúa el rendimiento de las diferentes heurísticas y del RF-HH en un conjunto de prueba.

    Genera estadísticas de rendimiento y figuras.

    Args:
        test_folder (str): Carpeta con las instancias de prueba.
        model (RandomForestClassifier): El modelo RF-HH entrenado.
        outdir (str): Directorio donde se guardarán las figuras.

    Returns:
        tuple[pd.DataFrame, float, float]: DataFrame con los resultados de profit
            por instancia, tiempo medio por instancia del rollout y del RF-HH.
    """
    files = list_instances(test_folder)
    rows, rollout_times = [], []
    # Listas para guardar las decisiones del primer paso para la matriz de confusión.
    oracle_first, rf_first = [], []

    for fname in files:
        path = os.path.join(test_folder, fname)
        # Inicializa la fila de resultados para la instancia actual.
        row = {"Filename": fname,
               "Category": "HARD" if "HARD" in fname else "EASY",
               "Type": instance_category(fname)}

        # Resuelve la instancia con cada heurística estática.
        for h in HEURISTICS:
            row[h] = KnapsackState.from_file(path).solve_static(h)

        # Resuelve la instancia con la política de rollout y mide el tiempo.
        state = KnapsackState.from_file(path)
        t0 = time.perf_counter()
        row["Rollout"] = solve_rollout(state)
        rollout_times.append(time.perf_counter() - t0)

        # Registra las decisiones del primer paso para el análisis de la matriz de confusión.
        state = KnapsackState.from_file(path)
        oracle_first.append(H_TO_IDX[rollout_choice(state)]) # La "verdad" del rollout.
        rf_first.append(int(model.predict(
            np.array([step_features(state)]))[0])) # La predicción del RF-HH.

        rows.append(row)

    # RF-HH: evaluación por lotes (soluciones idénticas, sobrecarga amortizada)
    # Prepara los estados de todas las instancias para la evaluación por lotes.
    states = [KnapsackState.from_file(os.path.join(test_folder, f))
              for f in files]
    t0 = time.perf_counter() # Mide el tiempo de ejecución del RF-HH por lotes.
    rf_profits = solve_rf_hh_batch(states, model)
    rf_total = time.perf_counter() - t0
    # Asigna los profits obtenidos por RF-HH a las filas correspondientes.
    for row, prof in zip(rows, rf_profits):
        row["RF-HH"] = prof

    df = pd.DataFrame(rows) # Crea el DataFrame final de resultados.

    # ---- Resumen de resultados ------------------------------------------
    methods = HEURISTICS + ["Rollout", "RF-HH"]
    print("\n=== Beneficio medio (N={}) ===".format(len(df)))
    print(df[methods].mean().round(2).to_string()) # Muestra el beneficio medio de cada método.

    # Realiza la prueba de Wilcoxon para comparar Rollout vs MAXPW.
    stat, p = wilcoxon(df["Rollout"], df["MAXPW"])
    wins = int((df["Rollout"] > df["MAXPW"]).sum()) # Cuenta las victorias de Rollout.
    print(f"\nWilcoxon Rollout vs MAXPW: p = {p:.3e}, "
          f"victorias {wins}/{len(df)} ({100 * wins / len(df):.1f}%) -- Cuántas veces Rollout fue mejor que MAXPW")

    # Realiza la prueba de Wilcoxon para comparar RF-HH vs MAXPW.
    stat2, p2 = wilcoxon(df["RF-HH"], df["MAXPW"])
    wins2 = int((df["RF-HH"] > df["MAXPW"]).sum()) # Cuenta las victorias de RF-HH.
    print(f"Wilcoxon RF-HH  vs MAXPW: p = {p2:.3e}, "
          f"victorias {wins2}/{len(df)} ({100 * wins2 / len(df):.1f}%) -- Cuántas veces RF-HH fue mejor que MAXPW")

    avg_rollout_time = np.mean(rollout_times)
    avg_rfhh_time = rf_total / len(df)
    print(f"\nTiempo medio/instancia: rollout {avg_rollout_time:.4f}s, "
          f"RF-HH (por lotes) {avg_rfhh_time:.4f}s")
    # Calcula qué porcentaje del beneficio del rollout el RF-HH logra capturar.
    ratio = df['RF-HH'].mean() / df['Rollout'].mean()
    print(f"RF-HH captura el {100 * ratio:.1f}% del beneficio del rollout")

    # Genera y guarda todas las figuras de visualización.
    make_figures(df, model, oracle_first, rf_first, outdir)
    print(f"Figuras escritas en {outdir}/")

    return df, avg_rollout_time, avg_rfhh_time


