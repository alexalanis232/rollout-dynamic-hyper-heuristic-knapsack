# Construcción del dataset (rollout como oráculo de etiquetado)
# ----------------------------------------------------------------------
def list_instances(folder: str) -> list[str]:
    """Lista los archivos de instancia de la mochila en una carpeta dada.

    Args:
        folder (str): La ruta de la carpeta.

    Returns:
        list[str]: Una lista ordenada de nombres de archivo de instancia (.kp).
    """
    return sorted(f for f in os.listdir(folder) if f.endswith(".kp"))


def build_rollout_dataset(folder: str, samples: int | None = None,
                          seed: int = SEED,
                          base: str = "MAXPW") -> pd.DataFrame:
    """Construye un dataset etiquetando cada estado visitado con la heurística seleccionada por el rollout.

    Las instancias se barajan con una semilla fija antes del submuestreo
    para que el subconjunto de entrenamiento no esté sesgado por el orden de los nombres de archivo.

    Args:
        folder (str): Carpeta que contiene las instancias del problema.
        samples (int | None): Número de instancias a usar para el entrenamiento (si es None, usa todas).
        seed (int): Semilla para la aleatoriedad.
        base (str): Heurística base para la política de rollout.

    Returns:
        pd.DataFrame: Un DataFrame con las características del estado y la heurística óptima ('BEST_H').
    """
    files = list_instances(folder)
    # Si se especifica un número de muestras, baraja y selecciona.
    if samples is not None and samples < len(files):
        random.Random(seed).shuffle(files)
        files = files[:samples]

    rows = []
    for fname in files:
        state = KnapsackState.from_file(os.path.join(folder, fname)) # Carga la instancia.
        while state.remaining.any(): # Itera hasta que no queden ítems por empacar.
            feats = step_features(state) # Calcula las características del estado actual.
            # Etiqueta con desempate a favor de la base: si varias heurísticas
            # empatan en beneficio simulado, la etiqueta es la base (MAXPW).
            h = rollout_choice(state, base, prefer_base_on_ties=True)
            # Agrega una fila al dataset con las características y la heurística elegida.
            rows.append(dict(zip(FEATURE_NAMES, feats), BEST_H=h))
            item = state.next_item(h) # Selecciona el ítem según la heurística.
            if item == -1: # Si no se puede empacar más ítems.
                break
            state.pack(item) # Empaca el ítem.
    return pd.DataFrame(rows)


